package com.company;

public class literals {
    public static void main(String[] args) {
        byte age = 23;
        char ch = 'A';
        float f1 = 5.6f;
        double d1 = 4.66D;
        boolean Q = true;
        String str = "Sahil";
        System.out.println(age);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1);
        System.out.println(Q);
        System.out.println(str);
    }
}
